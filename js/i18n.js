  lang() {
    return Store?.state?.settings?.language === 'pt' ? 'pt' : 'en';
  },

  locale() {
    return this.lang() === 'pt' ? 'pt-PT' : 'en-GB';
  },

  t(key, vars) {
    const lang = this.lang();
    let s = this.STRINGS[lang]?.[key] ?? this.STRINGS.en[key] ?? key;
    if (vars && typeof s === 'string') {
      for (const [k, v] of Object.entries(vars)) {
        s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
      }
    }
    return s;
  },

  viewTitle(view) {
    return this.t(`view.${view}`) || 'Candeias';
  },

  viewSubtitle(view) {
    return this.t(`view.${view}.sub`) || '';
  },

  apply() {
    const lang = this.lang();
    document.documentElement.lang = lang === 'pt' ? 'pt' : 'en';
    document.querySelectorAll('[data-i18n]').forEach((el) => {
      if (el.id === 'view-subtitle' || el.id === 'view-title') return;
      el.textContent = this.t(el.dataset.i18n);
    });
    const sub = document.getElementById('view-subtitle');
    if (sub && typeof App !== 'undefined' && App.currentView) {
      sub.textContent = this.viewSubtitle(App.currentView);
    }
    const title = document.getElementById('view-title');
    if (title && typeof App !== 'undefined' && App.currentView) {
      title.textContent = this.viewTitle(App.currentView);
    }
    const searchInp = document.querySelector('#search-trigger input');
    if (searchInp) searchInp.placeholder = this.t('topbar.search');
  },
